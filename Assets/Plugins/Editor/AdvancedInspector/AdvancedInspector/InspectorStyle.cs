namespace AdvancedInspector
{
    /// <summary>
    /// The different internal style.
    /// </summary>
    public enum InspectorStyle
    {
        /// <summary>
        /// No boxes, no nothing. Clean, similar to Unity default.
        /// </summary>
        Empty,
        /// <summary>
        /// Round boxes.
        /// </summary>
        Round,
        /// <summary>
        /// Unity flat style
        /// </summary>
        Flat
    }

    /// <summary>
    /// The separator's styles
    /// </summary>
    public enum SeparatorStyle
    {
        /// <summary>
        /// No separator visible
        /// </summary>
        Empty,
        /// <summary>
        /// The default gradient
        /// </summary>
        Gradient,
        /// <summary>
        /// The full bar
        /// </summary>
        Full
    }
}
